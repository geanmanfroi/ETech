    private String escreverTripulantesFaltantes() {
        Nave naveCorrente = (Nave) jComboBoxNave.getSelectedItem();
        ListSelectionModel tripulacao = jListTripulacao.getSelectionModel();
        int tamanhoTripulacao;
        if(tripulacao != null)
            tamanhoTripulacao = tripulacao.getMaxSelectionIndex() - tripulacao.getMinSelectionIndex();
        else
            tamanhoTripulacao = 0;
        return "" + (naveCorrente.getTripulacaoMaxima() - tamanhoTripulacao);
    }

    private boolean verificarGravacao() {
        return Integer.parseInt(jTextFieldTripulantesFaltantes.getText()) == 0 && (jFormattedTextFieldDataSaida.getText() != null
                || !jFormattedTextFieldDataSaida.getText().equalsIgnoreCase(""));
    }
